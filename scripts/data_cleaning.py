def clean_scheme_performance() -> pd.DataFrame:
    log("\n" + "=" * 70 + "\nTASK 3: Cleaning 07_scheme_performance.csv\n" + "=" * 70)

    df = pd.read_csv(RAW_DIR / "07_scheme_performance.csv")
    n_raw = len(df)

    numeric_cols = [
        "return_1yr_pct", "return_3yr_pct", "return_5yr_pct", "benchmark_3yr_pct",
        "alpha", "beta", "sharpe_ratio", "sortino_ratio", "std_dev_ann_pct",
        "max_drawdown_pct", "aum_crore", "expense_ratio_pct", "morningstar_rating",
    ]
    for col in numeric_cols:
        before_na = df[col].isna().sum()
        df[col] = pd.to_numeric(df[col], errors="coerce")
        new_na = df[col].isna().sum() - before_na
        if new_na:
            log(f"  '{col}': {new_na} non-numeric value(s) coerced to NaN")

    # Anomaly flags (reported, not auto-dropped - these are judgement calls
    # for an analyst, not hard data errors)
    flags = []
    flags.append(("negative_sharpe", (df["sharpe_ratio"] < 0).sum()))
    flags.append(("positive_max_drawdown (should be <= 0)", (df["max_drawdown_pct"] > 0).sum()))
    flags.append(("non_positive_std_dev (should be > 0)", (df["std_dev_ann_pct"] <= 0).sum()))
    flags.append(("beta_outside_0_to_2", (~df["beta"].between(0, 2)).sum()))
    flags.append(("morningstar_rating_outside_1_to_5", (~df["morningstar_rating"].between(1, 5)).sum()))
    out_of_range = ~df["expense_ratio_pct"].between(0.1, 2.5)
    flags.append(("expense_ratio_outside_0.1_to_2.5pct", out_of_range.sum()))

    for name, count in flags:
        log(f"  Anomaly check [{name}]: {count} row(s) flagged")

    dup = df.duplicated().sum()
    log(f"  Exact duplicate rows: {dup} (dropped)")
    df = df.drop_duplicates()

    log(f"  Final shape: {df.shape} (from {n_raw} raw rows). No automatic drops on flagged rows - "
        f"flags are informational since all checks passed clean on this dataset.")
    return df


