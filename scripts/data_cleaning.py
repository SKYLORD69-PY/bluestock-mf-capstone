def clean_investor_transactions() -> pd.DataFrame:
    log("\n" + "=" * 70 + "\nTASK 2: Cleaning 08_investor_transactions.csv\n" + "=" * 70)

    df = pd.read_csv(RAW_DIR / "08_investor_transactions.csv")
    n_raw = len(df)

    # Fix / standardise date formats
    df["transaction_date"] = pd.to_datetime(df["transaction_date"], format="mixed", dayfirst=False)

    # Standardise transaction_type to {SIP, Lumpsum, Redemption}
    type_map = {"sip": "SIP", "lumpsum": "Lumpsum", "lump sum": "Lumpsum", "redemption": "Redemption"}
    df["transaction_type"] = df["transaction_type"].str.strip().str.lower().map(type_map)
    bad_type = df["transaction_type"].isna()
    log(f"  Unrecognised transaction_type values: {bad_type.sum()} (dropped)")
    df = df[~bad_type]

    # Validate amount > 0
    bad_amount = (df["amount_inr"] <= 0) | df["amount_inr"].isna()
    log(f"  Rows with amount_inr <= 0 or null: {bad_amount.sum()} (dropped)")
    df = df[~bad_amount]

    # Validate kyc_status enum
    valid_kyc = {"Verified", "Pending"}
    df["kyc_status"] = df["kyc_status"].str.strip()
    bad_kyc = ~df["kyc_status"].isin(valid_kyc)
    log(f"  Rows with kyc_status outside {valid_kyc}: {bad_kyc.sum()} (dropped)")
    df = df[~bad_kyc]

    # Defensive enum checks on the other categorical columns (flag, don't drop
    # unless invalid - these all passed clean on inspection)
    for col, valid_set in {
        "city_tier": {"T30", "B30"},
        "gender": {"Male", "Female"},
        "payment_mode": {"UPI", "Net Banking", "Mandate", "Cheque"},
    }.items():
        n_bad = (~df[col].isin(valid_set)).sum()
        log(f"  '{col}' values outside {valid_set}: {n_bad}")

    # Remove exact duplicate rows
    dup = df.duplicated().sum()
    log(f"  Exact duplicate rows: {dup} (dropped)")
    df = df.drop_duplicates()

    df = df.sort_values(["transaction_date", "investor_id"]).reset_index(drop=True)
    df.insert(0, "tx_id", range(1, len(df) + 1))  # surrogate key - raw file has none
    df["transaction_date"] = df["transaction_date"].dt.strftime("%Y-%m-%d")

    log(f"  Final shape: {df.shape} (from {n_raw} raw rows). Generated surrogate tx_id 1..{len(df)}.")
    return df


