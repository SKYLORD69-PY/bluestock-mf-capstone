def clean_fund_master() -> pd.DataFrame:
    log("\n" + "=" * 70 + "\nTASK 1 (cont.): Cleaning 01_fund_master.csv (Day 1 anomaly fix)\n" + "=" * 70)
    df = pd.read_csv(RAW_DIR / "01_fund_master.csv")
    real_cols = [c for c in df.columns if not str(c).startswith("Unnamed")]
    n_raw, n_cols_raw = df.shape
    df = df[real_cols].dropna(subset=["amfi_code"]).copy()
    df["amfi_code"] = df["amfi_code"].astype(int)
    df["launch_date"] = pd.to_datetime(df["launch_date"], format="mixed", dayfirst=False).dt.strftime("%Y-%m-%d")
    dup = df.duplicated(subset=["amfi_code"]).sum()
    df = df.drop_duplicates(subset=["amfi_code"])
    log(f"  Dropped {n_raw - len(df)} blank row(s) and {n_cols_raw - len(real_cols)} junk column(s) "
        f"(the Day 1 structural defect). Duplicate amfi_codes: {dup}.")
    log(f"  Final shape: {df.shape}")
    return df


def clean_aum_by_fund_house() -> pd.DataFrame:
    log("\n--- Cleaning 03_aum_by_fund_house.csv ---")
    df = pd.read_csv(RAW_DIR / "03_aum_by_fund_house.csv")
    df["date"] = pd.to_datetime(df["date"], format="mixed", dayfirst=False).dt.strftime("%Y-%m-%d")
    bad = (df["aum_crore"] <= 0) | df["aum_crore"].isna()
    log(f"  Non-positive/null aum_crore rows: {bad.sum()} (dropped)")
    df = df[~bad].drop_duplicates(subset=["fund_house", "date"])
    log(f"  Final shape: {df.shape}")
    return df


def clean_monthly_sip_inflows() -> pd.DataFrame:
    log("\n--- Cleaning 04_monthly_sip_inflows.csv ---")
    df = pd.read_csv(RAW_DIR / "04_monthly_sip_inflows.csv")
    df = df.drop_duplicates(subset=["month"]).sort_values("month").reset_index(drop=True)
    # Cross-check the provided yoy_growth_pct against a recomputed value
    recomputed = df["sip_inflow_crore"].pct_change(12).mul(100).round(2)
    mismatch = ((df["yoy_growth_pct"] - recomputed).abs() > 0.5) & df["yoy_growth_pct"].notna()
    log(f"  yoy_growth_pct: {df['yoy_growth_pct'].isna().sum()} null (expected - no year-ago value "
        f"for the first 12 months). Mismatches vs. recomputed YoY: {mismatch.sum()}.")
    log(f"  Final shape: {df.shape}")
    return df


def clean_category_inflows() -> pd.DataFrame:
    log("\n--- Cleaning 05_category_inflows.csv ---")
    df = pd.read_csv(RAW_DIR / "05_category_inflows.csv")
    df = df.drop_duplicates(subset=["month", "category"]).sort_values(["month", "category"]).reset_index(drop=True)
    log(f"  Final shape: {df.shape}")
    return df


def clean_industry_folio_count() -> pd.DataFrame:
    log("\n--- Cleaning 06_industry_folio_count.csv ---")
    df = pd.read_csv(RAW_DIR / "06_industry_folio_count.csv")
    df = df.drop_duplicates(subset=["month"]).sort_values("month").reset_index(drop=True)
    component_sum = df[["equity_folios_crore", "debt_folios_crore", "hybrid_folios_crore", "others_folios_crore"]].sum(axis=1)
    mismatch = (df["total_folios_crore"] - component_sum).abs() > 0.05
    log(f"  total_folios_crore vs. sum-of-components mismatch (>0.05cr): {mismatch.sum()} row(s)")
    log(f"  Reporting cadence is irregular (not strictly quarterly) - kept as-is, this reflects real AMFI publication dates.")
    log(f"  Final shape: {df.shape}")
    return df


def clean_portfolio_holdings() -> pd.DataFrame:
    log("\n--- Cleaning 09_portfolio_holdings.csv ---")
    df = pd.read_csv(RAW_DIR / "09_portfolio_holdings.csv")
    df["portfolio_date"] = pd.to_datetime(df["portfolio_date"], format="mixed", dayfirst=False).dt.strftime("%Y-%m-%d")
    bad_weight = ~df["weight_pct"].between(0, 100)
    log(f"  weight_pct outside 0-100: {bad_weight.sum()} (dropped)")
    df = df[~bad_weight].drop_duplicates(subset=["amfi_code", "stock_symbol", "portfolio_date"])
    totals = df.groupby("amfi_code")["weight_pct"].sum()
    log(f"  Per-fund weight_pct totals range {totals.min():.2f}% - {totals.max():.2f}%: "
        f"every fund's disclosed holdings sum to ~100% (full portfolio, not a top-N excerpt). "
        f"{(totals - 100).abs().gt(0.5).sum()} fund(s) deviate from 100% by more than 0.5pt (true anomaly threshold).")
    log(f"  Final shape: {df.shape}")
    return df


def clean_benchmark_indices() -> pd.DataFrame:
    log("\n--- Cleaning 10_benchmark_indices.csv ---")
    df = pd.read_csv(RAW_DIR / "10_benchmark_indices.csv")
    df["date"] = pd.to_datetime(df["date"], format="mixed", dayfirst=False).dt.strftime("%Y-%m-%d")
    bad = (df["close_value"] <= 0) | df["close_value"].isna()
    log(f"  Non-positive/null close_value rows: {bad.sum()} (dropped)")
    df = df[~bad].drop_duplicates(subset=["date", "index_name"])
    log(f"  Final shape: {df.shape}")
    return df


def main() -> None:
    outputs = {
        "01_fund_master_clean.csv": clean_fund_master(),
        "02_nav_history_clean.csv": clean_nav_history(),
        "03_aum_by_fund_house_clean.csv": clean_aum_by_fund_house(),
        "04_monthly_sip_inflows_clean.csv": clean_monthly_sip_inflows(),
        "05_category_inflows_clean.csv": clean_category_inflows(),
        "06_industry_folio_count_clean.csv": clean_industry_folio_count(),
        "07_scheme_performance_clean.csv": clean_scheme_performance(),
        "08_investor_transactions_clean.csv": clean_investor_transactions(),
        "09_portfolio_holdings_clean.csv": clean_portfolio_holdings(),
        "10_benchmark_indices_clean.csv": clean_benchmark_indices(),
    }

    log("\n" + "=" * 70 + "\nSUMMARY\n" + "=" * 70)
    for name, df in outputs.items():
        out_path = PROCESSED_DIR / name
        df.to_csv(out_path, index=False)
        log(f"  {name:38s} -> {df.shape[0]:6d} rows x {df.shape[1]} cols")

    log_path = REPORTS_DIR / "day2_cleaning_log.txt"
    log_path.write_text("\n".join(LOG), encoding="utf-8")
    print(f"\nAll 10 cleaned CSVs written to {PROCESSED_DIR}")
    print(f"Cleaning log written to {log_path}")


if __name__ == "__main__":
    main()
