# ---------------------------------------------------------------------------
def clean_nav_history() -> pd.DataFrame:
    log("\n" + "=" * 70 + "\nTASK 1: Cleaning 02_nav_history.csv\n" + "=" * 70)

    df = pd.read_csv(RAW_DIR / "02_nav_history.csv")
    n_raw = len(df)

    df["date"] = pd.to_datetime(df["date"], format="mixed", dayfirst=False)

    # Validate NAV > 0
    bad_nav = (df["nav"] <= 0) | df["nav"].isna()
    log(f"  Rows with NAV <= 0 or null: {bad_nav.sum()} (dropped)")
    df = df[~bad_nav]

    # Remove exact duplicates and (amfi_code, date) duplicates - keep first
    exact_dups = df.duplicated().sum()
    key_dups = df.duplicated(subset=["amfi_code", "date"]).sum()
    log(f"  Exact duplicate rows: {exact_dups} (dropped)")
    log(f"  Duplicate (amfi_code, date) rows: {key_dups} (kept first occurrence)")
    df = df.drop_duplicates().drop_duplicates(subset=["amfi_code", "date"], keep="first")

    df = df.sort_values(["amfi_code", "date"]).reset_index(drop=True)

    # Reindex every fund onto a continuous CALENDAR-day range (its own min->max
    # date) and forward-fill NAV, so weekends/holidays inherit the last
    # published NAV. is_actual_trading_day=1 marks rows that genuinely came
    # from the source file; 0 marks a forward-filled (non-trading) day.
    filled_frames = []
    total_filled = 0
    for code, grp in df.groupby("amfi_code"):
        grp = grp.set_index("date")
        full_range = pd.date_range(grp.index.min(), grp.index.max(), freq="D")
        grp_full = grp.reindex(full_range)
        n_filled = grp_full["nav"].isna().sum()
        total_filled += n_filled
        grp_full["is_actual_trading_day"] = (~grp_full["nav"].isna()).astype(int)
        grp_full["nav"] = grp_full["nav"].ffill()
        grp_full["amfi_code"] = code
        grp_full.index.name = "date"
        filled_frames.append(grp_full.reset_index())

    df_full = pd.concat(filled_frames, ignore_index=True)
    log(f"  Calendar-day reindex per fund: {total_filled} weekend/holiday gap(s) "
        f"forward-filled across {df['amfi_code'].nunique()} funds "
        f"({n_raw} source rows -> {len(df_full)} continuous daily rows).")

    # Daily return, computed on the now-continuous series per fund.
    df_full = df_full.sort_values(["amfi_code", "date"]).reset_index(drop=True)
    df_full["daily_return_pct"] = (
        df_full.groupby("amfi_code")["nav"].pct_change().mul(100).fillna(0).round(4)
    )
    df_full["nav"] = df_full["nav"].round(4)
    df_full["date"] = df_full["date"].dt.strftime("%Y-%m-%d")
    df_full["amfi_code"] = df_full["amfi_code"].astype(int)

    out_cols = ["amfi_code", "date", "nav", "daily_return_pct", "is_actual_trading_day"]
    df_full = df_full[out_cols]

    log(f"  Final shape: {df_full.shape}. NAV<=0 check post-clean: {(df_full['nav'] <= 0).sum()}.")
    return df_full


